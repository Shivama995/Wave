function startjourney() {
    document.getElementById('artificialbody2').style.display = "block";
    document.getElementById('journeyvid').play();
    window.scrollTo(0, 6960);

    document.getElementById("journeyvid").onended = function () {
        document.getElementById('artificialbody2').style.display = "none";
    }

}

